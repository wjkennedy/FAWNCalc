int
nics4fnn(register int nodes,
register int ports)
{
	register short *p = &(fnntab[(ports - 2) * 5]);

	if ((ports < 2) || (ports > 1024)) return(1000);

	if (p[0] >= nodes) return(2);
	if (p[1] >= nodes) return(3);
	if (p[2] >= nodes) return(4);
	if (p[3] >= nodes) return(5);
	if (p[4] >= nodes) return(6);
	return(1000);
}

int
ports4fnn(register int nodes,
register int nics)
{
	register int ports;

	if (nics < 2) return(1000);
	if (nics > 6) nics = 6;

	for (ports=2; (ports <= 1024); ++ports) {
		if (fnntab[(nics - 2) + ((ports - 2) * 5)] >= nodes) {
			return(ports);
		}
	}
	return(1000);
}

int
nodes4fnn(register int ports,
register int nics)
{
	if ((ports < 2) || (ports > 1024)) return(1000);
	if (nics < 2) return(1000);
	if (nics > 6) nics = 6;

	return(fnntab[(nics - 2) + ((ports - 2) * 5)]);
}
