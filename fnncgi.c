int
len(register char **p)
{
	register int i = 0;

	while (*p != 0) {
		++i;
		++p;
	}
	return(i);
}

int
maxnics(register char **p)
{
	register int i, j;
	register int max = 0;

	for (i=0; *(p[0] + i); ++i) {
		register int k = 0;

		for (j=0; p[j]; ++j) {
			if (*(p[j] + i) == '1') ++k;
		}
		if (k > max) max = k;
	}

	return(max);
}

int
nicsused(register char **p)
{
	register int i = 0;
	register char *q;

	while (*p != 0) {
		register char *q = *p;

		while (*q) {
			if (*q == '1') ++i;
			++q;
		}
		++p;
	}
	return(i);
}


